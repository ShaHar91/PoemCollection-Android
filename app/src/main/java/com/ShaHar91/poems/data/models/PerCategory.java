package com.shahar91.poems.data.models;

public class PerCategory {
    private int id;
    private String title;
    private String writer;

    public PerCategory() {
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getWriter() {
        return writer;
    }
}
